class GlassBSDF : public BSDF
{
public:
	Texture* albedo;
	float intIOR;
	float extIOR;
	GlassBSDF() = default;
	GlassBSDF(Texture* _albedo, float _intIOR, float _extIOR)
	{
		albedo = _albedo;
		intIOR = _intIOR;
		extIOR = _extIOR;
	}
	Vec3 sample(const ShadingData& shadingData, Sampler* sampler, Colour& reflectedColour, float& pdf)
	{
		float eta_int = intIOR;
		float eta_ext = extIOR;
		Vec3 incoming = -shadingData.wo;
		Vec3 normal = shadingData.sNormal;
		if (Dot(incoming, normal) > 0) {
			eta_ext = intIOR;
			eta_int = extIOR;
			normal = -shadingData.sNormal;
		}
		float fresnel_value = ShadingHelper::fresnelDielectric(Dot(-incoming, normal), eta_int, eta_ext);
		Vec3 wi;
		if (sampler->next() < fresnel_value) {
			wi = shadingData.frame.toLocal(shadingData.wo);
			wi.x = -wi.x;
			wi.y = -wi.y;
			pdf = fresnel_value;
			wi = shadingData.frame.toWorld(wi);
			// Cos to cancel path throughput
			reflectedColour = albedo->sample(shadingData.tu, shadingData.tv) * fresnel_value / Dot(shadingData.gNormal, wi);
		} else {
			float eta = eta_ext / eta_int;
			float multiply = -eta * Dot(normal, incoming) - sqrtf(1 - eta * eta * (1 - powf(Dot(normal, incoming), 2)));
			wi = normal * multiply;
			wi = wi + incoming * eta;
			pdf = 1.0f - fresnel_value;
			reflectedColour = albedo->sample(shadingData.tu, shadingData.tv) * eta_ext * eta_ext / (eta_int * eta_int) * (1 - fresnel_value) / Dot(shadingData.gNormal, wi);
		}
		//reflectedColour = evaluate(shadingData, wi);
		return wi;
	}
	Colour evaluate(const ShadingData& shadingData, const Vec3& wi)
	{
		//return Colour(1, 1, 1);
		float fresnel_value;
		Colour refracted{};
		if (Dot(shadingData.wo, shadingData.sNormal) < 0) {
			fresnel_value = ShadingHelper::fresnelDielectric(Dot(-shadingData.wo, shadingData.sNormal), extIOR, intIOR);
			refracted = albedo->sample(shadingData.tu, shadingData.tv) * intIOR * intIOR / (extIOR * extIOR) * (1 - fresnel_value);
		} else {
			fresnel_value = ShadingHelper::fresnelDielectric(Dot(shadingData.wo, shadingData.sNormal), intIOR, extIOR);
			refracted = albedo->sample(shadingData.tu, shadingData.tv) * extIOR * extIOR / (intIOR * intIOR) * (1 - fresnel_value);
		}
		return (albedo->sample(shadingData.tu, shadingData.tv) * fresnel_value + refracted) / Dot(shadingData.gNormal, wi);
	}
	float PDF(const ShadingData& shadingData, const Vec3& wi)
	{
		return 0.0f;
	}
	bool isPureSpecular()
	{
		return true;
	}
	bool isTwoSided()
	{
		return false;
	}
	float mask(const ShadingData& shadingData)
	{
		return albedo->sampleAlpha(shadingData.tu, shadingData.tv);
	}
};

